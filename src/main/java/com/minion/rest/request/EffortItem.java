public class EffortItem {
	private String date;
	private float effort;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public float getEffort() {
		return effort;
	}
	public void setEffort(float effort) {
		this.effort = effort;
	}
	
	
}
