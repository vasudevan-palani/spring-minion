package com.minion.service.request;

import java.util.Date;

public class EffortItem {
	private Date date;
	private float effort;
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public float getEffort() {
		return effort;
	}
	public void setEffort(float effort) {
		this.effort = effort;
	}
	
	
}
